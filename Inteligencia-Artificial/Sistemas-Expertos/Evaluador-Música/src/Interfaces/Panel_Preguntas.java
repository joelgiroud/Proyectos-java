    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Rubro 1
        Respuesta res1 = new Respuesta(listapreg[0], jTextArea1.getText()); //Guardamos datos capturados
        Respuesta res2 = new Respuesta(listapreg[1], jTextArea2.getText());
        SisExp.califica(res1);                                              //Calificamos
        SisExp.califica(res2);
        
        if(SisExp.termina()){
            JOptionPane.showMessageDialog(rootPane, "Examen terminado");
            this.setVisible(false);
            panel_final = new Panel_Final(SisExp.getContador(), SisExp.respuestas_noev);
            panel_final.setDefaultCloseOperation(panel_final.EXIT_ON_CLOSE);
            this.setVisible(false);
            panel_final.setVisible(true);
            listapreg = new Pregunta[]{
                new Pregunta("","",false,""),
                new Pregunta("","",false,"")};
        }
        else{                           //Generamos nueva pregunta
            JOptionPane.showMessageDialog(rootPane, "Siguiente pregunta...");
            this.setVisible(false);
            listapreg = SisExp.obtenPar();
            jTextArea1.setText("");
            jTextArea2.setText("");     //Vaciamos campos de texto
            if(listapreg[0].getContenido()!=null){
                jLabel1.setText("<html>"+(String) listapreg[0].getContenido()+"</html>");
            }
            if(listapreg[1].getContenido()!=null){
                jLabel2.setText("<html>"+(String) listapreg[1].getContenido()+"</html>");
            }
            this.setVisible(true);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

